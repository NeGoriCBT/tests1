export function interpretHars(total) {
  if (total <= 6) return "Минимальная выраженность тревоги (условно)";
  if (total <= 17) return "Лёгкая тревога (условно)";
  if (total <= 24) return "Лёгкая — умеренная тревога (условно)";
  if (total <= 30) return "Умеренная — выраженная тревога (условно)";
  return "Тяжёлая тревога (условно)";
}
